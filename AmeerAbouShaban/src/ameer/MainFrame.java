    private void addBoughtToTable(Bought bought) {
        try {
            Class.forName("net.ucanaccess.jdbc.UcanaccessDriver");
            Connection con = DriverManager.getConnection("jdbc:ucanaccess://C:\\Users\\Alaa\\Documents\\NetBeansProjects\\"
                    + "AmeerAbouShaban\\Ameer.mdb", "", "");

            con.setAutoCommit(false);
            Statement s = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
            s.execute("select * from Boughts");
            ResultSet rs = s.getResultSet();
            rs.moveToInsertRow();
            rs.updateString("Name", bought.getName());
            rs.updateString("Type", bought.getType());
            rs.updateString("Phone", bought.getPhoneNumber());
            rs.updateString("Number", bought.getNumber());
            rs.updateString("Price", bought.getPrice());
            rs.updateString("Notes", bought.getNotes());
            rs.updateString("PayingWay", bought.getPayingWay());
            rs.updateString("BoughtDate", bought.getDate());
            int price = Integer.parseInt(bought.getPrice());
            int number = Integer.parseInt(bought.getNumber());
            int totalPrice = price * number;
            int paid = Integer.parseInt(bought.getPaid());
            String restOfMoney = "" + (totalPrice - paid);
            rs.updateString("RestOfMoney", restOfMoney);
            rs.insertRow();           
            con.commit();
            con.close();
            s.close();
            rs.close();
            JOptionPane.showMessageDialog(null, "تمت الإضافة بنجاح");
            JTextField fields[] = {et_notes, et_number, et_paid, et_phone_number, et_price, et_type, et_username, et_date};
            clearAllFields(fields);
        } catch (Exception ex) {
            System.out.println("" + ex.getMessage());
        }
    }

