    private void bt_login1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_login1ActionPerformed
        String number = et_number.getText();
        String price = et_price.getText();
        String paid = et_paid.getText();
        String name = et_username.getText();
        String date = et_date.getText();
        String type = et_type.getText();
        String phoneNumber = et_phone_number.getText();
        String typeOfPaying = jComboBox_type_of_paying.getSelectedItem().toString();
        String notes = et_notes.getText();
        String arrays[] = {name, number, type, price, paid, phoneNumber, typeOfPaying, date};
        if (checkStrings(arrays)) {
            try {
                Integer.parseInt(number);
                Integer.parseInt(price);
                Integer.parseInt(paid);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "الرجاء إدخال قيم صحيحة");
                return;
            }
            Bought bought = new Bought(name, type, phoneNumber, number, price, paid, notes, typeOfPaying, date);
            addBoughtToTable(bought);
        } else {
            JOptionPane.showMessageDialog(null, "جميع الحقول مطلوبة");
        }
    }//GEN-LAST:event_bt_login1ActionPerformed

